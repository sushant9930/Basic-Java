package com.basicjava;

public class avarage {

	public static void main(String[] args) 
	{
      float A=55f;          
      float B=60f;
      float C=78f;
      float D=81f;
      float E=95f;
      float X= A+B+C+D+E;   // Sum of above numbers.
      float Z= 5f;          // Total number of numbers.
      
      float average = (X/Z);     // Formula for find average. 
      System.out.println("Average of given data:" +average);
	}

}
