package com.basicjava;

public class AreaOfRectangular {

	public static void main(String[] args) 
	{
		
		int A = 30;  // length of rectangular 
		int B = 10;  // breadth of rectangular
		
		
		int Z = A*B; // For area of rectangular multiplication of above all sizes. area = length * breadth. 
		               
		
		System.out.println("Area of rectangular:" + Z);
		
	}

}
